package ru.job4j.math;

public class MathFunction {

    public static double sum(double first, double second) { // метод складывает два числа
        return first + second;
    }

    public static double multiply(double first, double second) { // метод умножает два числа
        return first * second;
    }

    public static double difference(double first, double second) { // метод расчитывает разность двух чисел
        return first - second;
    }

    public static double division(double first, double second) { // метод делит одно число на другое
        return first / second;
    }
}

